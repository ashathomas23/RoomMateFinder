package com.utd.rfinder.beans;

public class FloorPlan {
	
	private int mFloorPlanId;
	private int mNoOfRooms;
	private int mNoOfBathRooms;
	private int mArea;
	private String mUtilities;
	private double mRent;
	private int mFloor;
	public int getFloorPlanId() {
		return mFloorPlanId;
	}
	public void setFloorPlanId(int aFloorPlanId) {
		mFloorPlanId = aFloorPlanId;
	}
	public int getNoOfRooms() {
		return mNoOfRooms;
	}
	public void setNoOfRooms(int aNoOfRooms) {
		mNoOfRooms = aNoOfRooms;
	}
	public int getNoOfBathRooms() {
		return mNoOfBathRooms;
	}
	public void setNoOfBathRooms(int aNoOfBathRooms) {
		mNoOfBathRooms = aNoOfBathRooms;
	}
	public int getArea() {
		return mArea;
	}
	public void setArea(int aArea) {
		mArea = aArea;
	}
	public String getUtilities() {
		return mUtilities;
	}
	public void setUtilities(String aUtilities) {
		mUtilities = aUtilities;
	}
	public double getRent() {
		return mRent;
	}
	public void setRent(double aRent) {
		mRent = aRent;
	}
	public int getFloor() {
		return mFloor;
	}
	public void setFloor(int aFloor) {
		mFloor = aFloor;
	}
	

}
